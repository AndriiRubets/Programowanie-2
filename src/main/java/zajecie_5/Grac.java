package zajecie_5;


public class Grac {
    private String imie;
    private int iloscPunktow;


    public Grac(String imie, int iloscPunktow) {
        this.imie = imie;
        this.iloscPunktow = iloscPunktow;
    }

    public String getImie() {
        return imie;
    }

    public int getIloscPunktow() {
        return iloscPunktow;
    }

}
